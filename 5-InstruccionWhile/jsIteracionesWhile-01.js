/*
al presionar el botón mostrar 10 repeticiones 
con números ASCENDENTE, desde el 1 al 10.*/
function mostrar()
{
	var variableDeControl = 0; //declarar y dar valor

	while (variableDeControl < 10)  //variable de control (condicion)
	{
		variableDeControl ++;
		console.log(variableDeControl);
	}
	
	
	
	
	alert('iteración while');
}//FIN DE LA FUNCIÓN